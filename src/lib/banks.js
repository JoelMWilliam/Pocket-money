function normalizeSender(text) {
  return String(text).toUpperCase().replace(/[^A-Z0-9]/g, '')
}

function tokenize(text) {
  return String(text).toUpperCase().split(/[^A-Z0-9]+/).filter(Boolean)
}

function isSubsequence(a, b) {
  let i = 0
  for (let j = 0; j < b.length && i < a.length; j++) {
    if (a[i] === b[j]) i++
  }
  return i === a.length
}

export function findBankBySenderId(sender) {
  if (!sender) return null
  const norm = normalizeSender(sender)
  if (!norm) return null

  let match = BANKS.find(b => b.senderIds.some(s => normalizeSender(s) === norm))
  if (match) return match

  if (norm.length >= 4) {
    match = BANKS.find(b => b.senderIds.some(s => {
      const n = normalizeSender(s)
      return n.startsWith(norm) || norm.startsWith(n)
    }))
    if (match) return match
  }

  match = BANKS.find(b => b.senderIds.some(s => {
    const n = normalizeSender(s)
    if (n.length < 4 || norm.length < 4) return false
    if (Math.abs(n.length - norm.length) > 2) return false
    return isSubsequence(n, norm) || isSubsequence(norm, n)
  }))

  return match || null
}

export function findBankByName(name) {
  if (!name) return null
  const norm = normalizeSender(name)
  const inputTokens = tokenize(name)
  if (!inputTokens.length) return null

  for (const bank of BANKS) {
    const haystacks = [bank.name, ...bank.shortNames, ...bank.aliases, ...bank.senderIds]
    if (haystacks.some(h => normalizeSender(h) === norm)) return bank

    const tokens = haystacks.flatMap(tokenize)
    if (inputTokens.every(t => tokens.includes(t))) return bank
  }

  return null
}

export function getBankById(id) {
  return BANKS.find(b => b.id === id) || null
}

export function getAllSenderIds() {
  return [...new Set(BANKS.flatMap(b => b.senderIds))]
}

export function inferAccountType(bankId) {
  const bank = getBankById(bankId)
  if (!bank) return 'bank'
  if (bank.defaultAccountType) return bank.defaultAccountType
  if (bank.type === 'wallet') return 'wallet'
  if (bank.type === 'digital') return 'digital'
  return 'bank'
}

export function isDigitalWallet(bankId) {
  const bank = getBankById(bankId)
  if (!bank) return false
  return bank.type === 'wallet' || bank.type === 'digital'
}
